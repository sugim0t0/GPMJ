        eye4.add_tile(self.all_tiles[gpmj.gpmj.Suits.WINDS][13])
        eyes.append(eye4)
        eye5 = gpmj.gpmj.Eye()
        eye5.add_tile(self.all_tiles[gpmj.gpmj.Suits.DRAGONS][0])
        last_tile = self.all_tiles[gpmj.gpmj.Suits.DRAGONS][1]
        eye5.add_tile(last_tile)
        eyes.append(eye5)
        eye6 = gpmj.gpmj.Eye()
